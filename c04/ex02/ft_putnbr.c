#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		i;
	char	number[11];

	i = 0;
	if (nb == 0)
		write(1, "0", 1);
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	if (nb < 0 && nb != -2147483648)
	{
		write(1, "-", 1);
		nb = (nb * -1);
	}
	while (nb != 0 && nb != -2147483648)
	{
		number[i] = (48 + (nb % 10));
		nb = nb / 10;
		i++;
	}
	while (i > 0)
	{
		write(1, &number[i - 1], 1);
		i--;
	}
}
